# Bieu thuc If...else lồng nhau

dtb = float(input('Nhập điểm trung bình: '))
if dtb >= 9:
    print('Giỏi')
elif dtb >= 7:
    print('Khá')
elif dtb >= 5:
    print('Trung bình')
else:
    print('Yếu')


