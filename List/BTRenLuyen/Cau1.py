lst = [3, 0, 1, 5, 2]
x = 2

print('lst[0] =', lst[0]) # = 3
print('lst[3] =', lst[3]) # = 5
print('lst[x] =', lst[x]) # = 1
print('lst[-x] =', lst[-x]) # = 5
print('lst[x+1] =', lst[x+1]) # = 5
print('lst[x] + 1 =', lst[x] + 1) # = 2
print('lst[lst[x]] =', lst[lst[x]]) # = 0
print('lst[lst[lst[x]]] =', lst[lst[lst[x]]]) # = 3
